package start.variable;

public class Var5 {
    public static void main(String[] args) {
        // 1. 변수 선언과 초기화 각각 따로
        int a;
        a = 5;
        System.out.println(a);

        // 2. 변수 선언과 초기화를 동시에
        int b = 10;
        System.out.println(b);

        int c = 20, d = 30;
        System.out.println(c);
        System.out.println(d);
    }
}
